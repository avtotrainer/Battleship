# 🎯 თემა: პირობითი ოპერატორები (if / else)
# 🧠 მიზანი: შევძლოთ გადაწყვეტილების მიღება — Treffer (დარტყმა) თუ Miss (მარცხი)

print("ნავების ომი — გაკვეთილი №3")

# ჩვენი ნავის პოზიცია
ship_x = 3
ship_y = 5

# მოთამაშის სროლა
shot_x = int(input("შეიყვანე სროლის X კოორდინატი: "))
shot_y = int(input("შეიყვანე სროლის Y კოორდინატი: "))

# ⚙️ პირობითი გამოთქმა
if ship_x == shot_x and ship_y == shot_y:
    print("🎯 Treffer! ზუსტად მოხვდი ნავს!")
else:
    print("💨 Miss! აცდა, სცადე თავიდან.")

# 🧩 დამატებითი მაგალითი:
temperature = 20

if temperature > 25:
    print("ცხელი დღეა.")
else:
    print("გაგრილდა!")

