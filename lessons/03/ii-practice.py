ship_x = 2
ship_y = 4

# ­ЪЉЅ рЃюрЃљрЃЉрЃўрЃ»рЃў 2: рЃЏрЃўрЃўрЃдрЃћ рЃАрЃарЃЮрЃџрЃўрЃА рЃЎрЃЮрЃЮрЃарЃЊрЃўрЃюрЃљрЃбрЃћрЃЉрЃў
shot_x = int(input("рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћ рЃАрЃарЃЮрЃџрЃўрЃА X рЃЎрЃЮрЃЮрЃарЃЊрЃўрЃюрЃљрЃбрЃў: "))
shot_y = int(input("рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћ рЃАрЃарЃЮрЃџрЃўрЃА Y рЃЎрЃЮрЃЮрЃарЃЊрЃўрЃюрЃљрЃбрЃў: "))

# ­ЪЉЅ рЃюрЃљрЃЉрЃўрЃ»рЃў 3: рЃерЃћрЃљрЃЏрЃЮрЃгрЃЏрЃћ рЃЊрЃљрЃћрЃЏрЃЌрЃ«рЃЋрЃљ рЃЌрЃБ рЃљрЃарЃљ
if shot_x == ship_x and shot_y == ship_y:
    print("­Ъј» Treffer! рЃќрЃБрЃАрЃбрЃљрЃЊ рЃЏрЃЮрЃ«рЃЋрЃЊрЃў рЃюрЃљрЃЋрЃА!")
else:
    # рЃЊрЃљрЃЏрЃљрЃбрЃћрЃЉрЃўрЃЌрЃў рЃърЃўрЃарЃЮрЃЉрЃћрЃЉрЃў рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ X рЃљрЃю рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ Y рЃерЃћрЃАрЃљрЃЉрЃљрЃЏрЃўрЃАрЃЮрЃЉрЃљрЃќрЃћ
    if shot_x == ship_x and shot_y != ship_y:
        print("­ЪЉЅ рЃўрЃњрЃўрЃЋрЃћ рЃАрЃЋрЃћрЃбрЃў! рЃерЃћрЃфрЃЋрЃљрЃџрЃћ Y.")
    elif shot_y == ship_y and shot_x != ship_x:
        print("­ЪЉЅ рЃўрЃњрЃўрЃЋрЃћ рЃЏрЃгрЃЎрЃарЃўрЃЋрЃў! рЃерЃћрЃфрЃЋрЃљрЃџрЃћ X.")
    else:
        print("­Ъње рЃАрЃарЃБрЃџрЃў рЃЏрЃљрЃарЃфрЃ«рЃў!")

# ­ЪЊІ рЃЊрЃљрЃЋрЃљрЃџрЃћрЃЉрЃљ:
