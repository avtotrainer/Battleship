# ­Ъј» рЃЌрЃћрЃЏрЃљ: рЃфрЃўрЃЎрЃџрЃћрЃЉрЃў (while / for)
# ­ЪДа рЃЏрЃўрЃќрЃљрЃюрЃў: рЃЋрЃўрЃАрЃгрЃљрЃЋрЃџрЃЮрЃЌ рЃЏрЃЮрЃЦрЃЏрЃћрЃЊрЃћрЃЉрЃўрЃА рЃњрЃљрЃЏрЃћрЃЮрЃарЃћрЃЉрЃљ рЃўрЃЦрЃљрЃЏрЃЊрЃћ, рЃАрЃљрЃюрЃљрЃЏ рЃърЃўрЃарЃЮрЃЉрЃљ рЃерЃћрЃАрЃарЃБрЃџрЃЊрЃћрЃЉрЃљ (True).

print("рЃюрЃљрЃЋрЃћрЃЉрЃўрЃА рЃЮрЃЏрЃў Рђћ рЃњрЃљрЃЎрЃЋрЃћрЃЌрЃўрЃџрЃў Рёќ4")

# ­ЪДЙ рЃЎрЃЮрЃЏрЃћрЃюрЃбрЃљрЃарЃћрЃЉрЃў рЃърЃўрЃЌрЃЮрЃюрЃерЃў
# рЃЎрЃЮрЃЏрЃћрЃюрЃбрЃљрЃарЃў рЃўрЃгрЃДрЃћрЃЉрЃљ рЃАрЃўрЃЏрЃЉрЃЮрЃџрЃЮрЃЌрЃў '#'
# рЃЎрЃЮрЃЏрЃћрЃюрЃбрЃљрЃарЃў рЃљрЃа рЃАрЃарЃБрЃџрЃЊрЃћрЃЉрЃљ, рЃБрЃЉрЃарЃљрЃџрЃЮрЃЊ рЃ«рЃАрЃюрЃўрЃА рЃарЃљрЃА рЃљрЃЎрЃћрЃЌрЃћрЃЉрЃА рЃЎрЃЮрЃЊрЃў.
# рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃљ рЃЎрЃЮрЃЏрЃћрЃюрЃбрЃљрЃарЃА рЃљрЃа рЃЎрЃўрЃЌрЃ«рЃБрЃџрЃЮрЃЉрЃА Рђћ рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃљрЃЊрЃљрЃЏрЃўрЃљрЃюрЃў рЃЎрЃўрЃЌрЃ«рЃБрЃџрЃЮрЃЉрЃА.
# рЃЎрЃЮрЃЏрЃћрЃюрЃбрЃљрЃарЃћрЃЉрЃў рЃФрЃљрЃџрЃўрЃљрЃю рЃЏрЃюрЃўрЃерЃЋрЃюрЃћрЃџрЃЮрЃЋрЃљрЃюрЃўрЃљ, рЃарЃљрЃЊрЃњрЃљрЃю:
# 1№ИЈРЃБ рЃ«рЃАрЃюрЃўрЃА рЃЎрЃЮрЃЊрЃўрЃА рЃљрЃќрЃарЃА;
# 2№ИЈРЃБ рЃћрЃ«рЃЏрЃљрЃарЃћрЃЉрЃљ рЃАрЃ«рЃЋрЃљрЃА (рЃљрЃю рЃерЃћрЃюрЃА рЃЏрЃЮрЃЏрЃљрЃЋрЃљрЃџ рЃЌрЃљрЃЋрЃА) рЃБрЃЎрЃћрЃЌ рЃњрЃљрЃўрЃњрЃЮрЃА, рЃарЃљ рЃ«рЃЊрЃћрЃЉрЃљ;
# 3№ИЈРЃБ рЃЌрЃљрЃЋрЃўрЃЊрЃљрЃю рЃњрЃЋрЃљрЃфрЃўрЃџрЃћрЃЉрЃА рЃерЃћрЃфрЃЊрЃЮрЃЏрЃћрЃЉрЃА рЃЊрЃўрЃЊ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃћрЃЉрЃерЃў.
# рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃў:
x = 5  # рЃћрЃА рЃфрЃЋрЃџрЃљрЃЊрЃў рЃўрЃюрЃљрЃ«рЃљрЃЋрЃА рЃюрЃљрЃЋрЃўрЃА рЃърЃЮрЃќрЃўрЃфрЃўрЃљрЃА

# ­ЪДЕ рЃљрЃ«рЃџрЃљ рЃЋрЃўрЃАрЃгрЃљрЃЋрЃџрЃЮрЃЌ рЃљрЃ«рЃљрЃџрЃў рЃфрЃюрЃћрЃЉрЃљ: True рЃЊрЃљ False
# True  рЃюрЃўрЃерЃюрЃљрЃЋрЃА РђърЃГрЃћрЃерЃЏрЃљрЃарЃўрЃбрЃўрЃљРђю (рЃърЃўрЃарЃЮрЃЉрЃљ рЃАрЃгрЃЮрЃарЃўрЃљ)
# False рЃюрЃўрЃерЃюрЃљрЃЋрЃА РђърЃЏрЃфрЃЊрЃљрЃарЃўрЃљРђю (рЃърЃўрЃарЃЮрЃЉрЃљ рЃљрЃарЃљрЃАрЃгрЃЮрЃарЃўрЃљ)

# рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃћрЃЉрЃў:
print(3 > 1)    # рЃГрЃћрЃерЃЏрЃљрЃарЃўрЃбрЃўрЃљ Рєњ True
print(2 == 5)   # рЃЏрЃфрЃЊрЃљрЃарЃўрЃљ Рєњ False
print(10 <= 10) # рЃГрЃћрЃерЃЏрЃљрЃарЃўрЃбрЃўрЃљ Рєњ True

# рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃљрЃЊ рЃерЃћрЃюрЃљрЃ«рЃЋрЃљ рЃфрЃЋрЃџрЃљрЃЊрЃерЃў:
is_sunny = True
is_raining = False
print("рЃЏрЃќрЃћ рЃљрЃюрЃљрЃЌрЃћрЃЉрЃА?", is_sunny)
print("рЃгрЃЋрЃўрЃЏрЃА?", is_raining)

# рЃљрЃ«рЃџрЃљ рЃЊрЃљрЃЋрЃБрЃЎрЃљрЃЋрЃерЃўрЃарЃЮрЃЌ рЃћрЃА рЃфрЃўрЃЎрЃџрЃћрЃЉрЃА:
ship_x = 3
ship_y = 2
hit = False  # рЃ»рЃћрЃа Treffer рЃљрЃарЃљрЃљ

while not hit:  # "рЃАрЃљрЃюрЃљрЃЏ рЃљрЃа рЃЏрЃЮрЃ«рЃЊрЃљ Treffer"
    print("­ЪњБ рЃљрЃ«рЃљрЃџрЃў рЃАрЃарЃЮрЃџрЃљ!")
    shot_x = int(input("рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћ X рЃЎрЃЮрЃЮрЃарЃЊрЃўрЃюрЃљрЃбрЃў: "))
    shot_y = int(input("рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћ Y рЃЎрЃЮрЃЮрЃарЃЊрЃўрЃюрЃљрЃбрЃў: "))

    if shot_x == ship_x and shot_y == ship_y:
        print("­Ъј» Treffer! рЃюрЃљрЃЋрЃў рЃњрЃљрЃюрЃљрЃЊрЃњрЃБрЃарЃћрЃЉрЃБрЃџрЃўрЃљ!")
        hit = True
    else:
        print("­Ъње рЃљрЃфрЃЊрЃљ, рЃАрЃфрЃљрЃЊрЃћ рЃЌрЃљрЃЋрЃўрЃЊрЃљрЃю!")

print("рЃЌрЃљрЃЏрЃљрЃерЃў рЃЊрЃљрЃАрЃарЃБрЃџрЃЊрЃљ, рЃњрЃўрЃџрЃЮрЃфрЃљрЃЋ рЃњрЃљрЃЏрЃљрЃарЃ»рЃЋрЃћрЃЉрЃљрЃА!")

print("\n­ЪЊІ рЃљрЃ«рЃџрЃљ рЃЊрЃљрЃЋрЃЌрЃЋрЃљрЃџрЃЮрЃЌ рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃў 1-рЃЊрЃљрЃю 5-рЃЏрЃЊрЃћ:")
for number in range(1, 6):
    print("рЃарЃўрЃфрЃ«рЃЋрЃў:", number)

# ­ЪЊў рЃљрЃ«рЃАрЃюрЃљ:
# while Рђћ рЃњрЃљрЃЏрЃћрЃЮрЃарЃћрЃЉрЃљ рЃАрЃљрЃюрЃљрЃЏ рЃърЃўрЃарЃЮрЃЉрЃљ True-рЃљ
# for Рђћ рЃњрЃљрЃюрЃЏрЃћрЃЮрЃарЃћрЃЉрЃљ рЃњрЃљрЃюрЃАрЃљрЃќрЃдрЃЋрЃарЃБрЃџрЃў рЃЊрЃўрЃљрЃърЃљрЃќрЃЮрЃюрЃўрЃЌ
# True/False Рђћ рЃгрЃДрЃЋрЃћрЃбрЃА, рЃерЃћрЃАрЃарЃБрЃџрЃЊрЃћрЃА рЃЌрЃБ рЃљрЃарЃљ рЃЏрЃЮрЃЦрЃЏрЃћрЃЊрЃћрЃЉрЃљ
