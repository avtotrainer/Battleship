# სანამ Treffer არ მოხდება ან სანამ ყველა ცდა არ ამოიწურება.

ship_x = 4
ship_y = 3
tries = 3  # სამი მცდელობა

print("🎯 სამივე სროლით უნდა გაანადგურო ნავი!")

while tries > 0:
    print("\nმცდელობები დარჩა:", tries)
    shot_x = int(input("X: "))
    shot_y = int(input("Y: "))

    if shot_x == ship_x and shot_y == ship_y:
        print("🎉 Treffer! ნავი განადგურებულია!")
        break
    else:
        print("💨 აცდა... სცადე თავიდან!")
        tries = tries - 1

if tries == 0:
    print("❌ მცდელობები ამოიწურა! თამაში დასრულდა.")

