# 🧩 დავალება №2 — ჩემი ნავი
# ამოცანა: შექმენი პროგრამა, რომელიც დაადგენს, მოხვდა თუ არა ტყვია ნავს.

# 👉 ნაბიჯი 1: შეიყვანე ნავის კოორდინატები (x, y)
ship_x = int(input("შეიყვანე ნავის X კოორდინატი: "))
ship_y = int(input("შეიყვანე ნავის Y კოორდინატი: "))

# 👉 ნაბიჯი 2: შეიყვანე სროლის კოორდინატები
shot_x = int(input("სროლის X კოორდინატი: "))
shot_y = int(input("სროლის Y კოორდინატი: "))

# 👉 ნაბიჯი 3: შეამოწმე დაემთხვა თუ არა
hit = (ship_x == shot_x) and (ship_y == shot_y)

# 👉 შედეგი
if hit:
    print("🎯 ზუსტად მოხვდი ნავს!")
else:
    print("💨 აცდა, სცადე კიდევ ერთხელ!")

